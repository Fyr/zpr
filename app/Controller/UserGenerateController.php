    private function __updateUsersCities($country_id, $users) {
        $updated = 0;

        $city = $this->City->find('first', array('conditions' => array('City.country_id' => $country_id),
                                                 'fields'     => array('id'),
                                                 'order'      => array('rand()')));

        $user_ids = Set::extract('/id', $users);

        $res = $this->User->updateAll(
            array('city_id' => $city['City']['id']),
            array('id' => $user_ids)
        );

        if (!$res) {
            echo "Can't update users<br>";
        } else {
            $updated = count($user_ids);
        }

        return $updated;
    }

    public function cities() {
        $this->autoRender = false;
        ini_set('max_execution_time', 3600);

        $users = $this->User->find('all', array('conditions' => array('city_id' => null),
                                                'order'      => array('country_id')));

        $user_group_cnt = 10;
        $updated = 0;

//        echo '<pre>';
//        print_r($users);
//        echo '</pre>';

        $old_country_id = null;
        $user_array = array();
        foreach ($users as $user_row) {
            $user = $user_row['User'];

            if ($old_country_id != $user['country_id'] or count($user_array) >= $user_group_cnt) {
                if ($old_country_id and !empty($user_array)) {
                    $updated += $this->__updateUsersCities($old_country_id, $user_array);
                }
                $user_array = array();
            }

            $old_country_id = $user['country_id'];
            $user_array[] = $user;
        }
        if ($old_country_id and !empty($user_array)) {
            $updated += $this->__updateUsersCities($old_country_id, $user_array);
        }

        echo "<br><br>updated - {$updated}<br>";
    }

